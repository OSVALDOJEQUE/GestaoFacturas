package Model.Entidade;

public class Tipoproduto{
    
    private int codTipoProduto;
    private String DescrTipoProduto,pesquisa;
    
    public Tipoproduto(){
    }

    public int getCodTipoProduto() {
        return codTipoProduto;
    }

    public void setCodTipoProduto(int codTipoProduto) {
        this.codTipoProduto = codTipoProduto;
    }

    public String getDescrTipoProduto() {
        return DescrTipoProduto;
    }

    public void setDescrTipoProduto(String DescrTipoProduto) {
        this.DescrTipoProduto = DescrTipoProduto;
    }

    public String getPesquisa() {
        return pesquisa;
    }

    public void setPesquisa(String pesquisa) {
        this.pesquisa = pesquisa;
    }
    
    
    
    
    


}
