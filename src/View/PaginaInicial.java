     public void upTime(JLabel jlHora) {
        new Thread() {
            
            @Override
            public void run() {
                while (true) {
                    try {
                        jlHora.setText(new Time(System.currentTimeMillis()).toString());
                        sleep(1000);
                    } catch (InterruptedException e) {
                        JOptionPane.showMessageDialog(jlHora, "Erro actualizar a hoara, Erro F006");
                    }
                }
            }
        }.start();
    }
    
    
    public void PreencherTabela(String sql){
    ArrayList dados=new ArrayList();
    String[] colunas=new String[]{"ID","Nome","Endereco","Telefone"};
    con=BDconexao.getconnection();
    conex.executaSql(sql);
    try{
    conex.rs.first();
    do{
       dados.add(new Object[]{conex.rs.getInt("CodFornecedor"),conex.rs.getString("Nome"),
       conex.rs.getString("Endereco"),conex.rs.getInt("Telefone")});
      
    }while(conex.rs.next());
    }catch(SQLException ex){
    
       JOptionPane.showMessageDialog(null,"Busque novamente");
    } 
    Tabela modelo=new Tabela(dados,colunas);
  
   
    jTableForncedor.setModel(modelo);
    jTableForncedor.getColumnModel().getColumn(0).setPreferredWidth(80);
    jTableForncedor.getColumnModel().getColumn(0).setResizable(false);
     jTableForncedor.getColumnModel().getColumn(1).setPreferredWidth(241);
    jTableForncedor.getColumnModel().getColumn(1).setResizable(false);
     jTableForncedor.getColumnModel().getColumn(2).setPreferredWidth(300);
    jTableForncedor.getColumnModel().getColumn(2).setResizable(false);
     jTableForncedor.getColumnModel().getColumn(3).setPreferredWidth(80);
    jTableForncedor.getColumnModel().getColumn(3).setResizable(false);
    jTableForncedor.getTableHeader().setReorderingAllowed(false);
    jTableForncedor.setAutoResizeMode(jTableForncedor.AUTO_RESIZE_OFF);
    jTableForncedor.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    
    
    
    BDconexao.fecharConexao(con);
    }
