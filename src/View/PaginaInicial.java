    private void jTableProdutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProdutoMouseClicked
        String nome=""+jTableProduto.getValueAt(jTableProduto.getSelectedRow(),1);
        con=BDconexao.getconnection();

        conex.executaSql("SELECT CodProduto,Descr,CodFornecedor,Nome,PrecoUnit,DescrTipoProduto from Fornecedor "
            + "NATURAL JOIN produto NATURAL join tipoproduto where descr='"+nome+"'");
        try {
            conex.rs.first();
            jTextFieldArtigo.setText(String.valueOf(conex.rs.getInt("CodProduto")));
            jTextFieldProduto.setText(conex.rs.getString("Descr"));
            jTextFieldQtd.setText("1");
            // jComboBoxFornecedor.setSelectedItem(conex.rs.getString("Nome"));
            jTextFieldPrecUnit.setText(String.valueOf(conex.rs.getDouble("PrecoUnit")));
            jTextFieldTipo.setText(conex.rs.getString("DescrTipoProduto"));

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Erro Ao preecher do MauseClicked");
        }
        BDconexao.fecharConexao(con);
    }//GEN-LAST:event_jTableProdutoMouseClicked

