    public void PreencherTabela(String sql){
    ArrayList dados=new ArrayList();
    String[] colunas=new String[]{"ID","Nome","Tipo"};
    con=BDconexao.getconnection();
    conex.executaSql(sql);
    try{
    conex.rs.first();
    do{
       dados.add(new Object[]{conex.rs.getInt("CodUsuario"),conex.rs.getString("Nome"),
           conex.rs.getString("Tipo")});
      
    }while(conex.rs.next());
    }catch(SQLException ex){
    
       JOptionPane.showMessageDialog(null,"Erro Ao preecher O arreyList");
    } 
    Tabela modelo=new Tabela(dados,colunas);
  
   
    jTableUsuario.setModel(modelo);
    jTableUsuario.getColumnModel().getColumn(0).setPreferredWidth(120);
    jTableUsuario.getColumnModel().getColumn(0).setResizable(false);
     jTableUsuario.getColumnModel().getColumn(1).setPreferredWidth(350);
    jTableUsuario.getColumnModel().getColumn(1).setResizable(false);
     jTableUsuario.getColumnModel().getColumn(1).setPreferredWidth(300);
    jTableUsuario.getColumnModel().getColumn(1).setResizable(false);
    jTableUsuario.setAutoResizeMode(jTableUsuario.AUTO_RESIZE_OFF);
    jTableUsuario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    
    BDconexao.fecharConexao(con);
    }
