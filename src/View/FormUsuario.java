    private void jTableUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableUsuarioMouseClicked
        String nome=""+jTableUsuario.getValueAt(jTableUsuario.getSelectedRow(),1);
       con=BDconexao.getconnection();
       conex.executaSql("Select CodUsuario,nome, tipo from Usuario where Nome='"+nome+"'");
       try {
           conex.rs.first();
           jTextFieldCod.setText(String.valueOf(conex.rs.getInt("CodUsuario")));
            jTextFieldNome.setText(conex.rs.getString("Nome"));
           jComboBoxTipo.setSelectedItem(conex.rs.getString("Tipo"));
           
         
       } catch (SQLException ex) {
          JOptionPane.showMessageDialog(null,"Erro Ao preecher do MauseClicked");
       }
       BDconexao.fecharConexao(con);
       jButtonEditar.setEnabled(true);
       jButtonExcluir.setEnabled(true);
    }//GEN-LAST:event_jTableUsuarioMouseClicked

