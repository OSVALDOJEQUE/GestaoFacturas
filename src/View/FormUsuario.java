    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
         if(jTextFieldNome.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Preecha o campo nome");
            jTextFieldNome.requestFocus();
        }else  if(jTextFieldConfSenha.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null,"o campo confirmar");
            jTextFieldConfSenha.requestFocus();
        }else if(jPasswordFieldSenha.getText().isEmpty()){
        JOptionPane.showMessageDialog(null,"Preecha o campo senha");
            jPasswordFieldSenha.requestFocus();
        }
         else
      
      if(flag==1){
       
        umode.setNome(jTextFieldNome.getText());
        umode.setSenha(jPasswordFieldSenha.getText());
        umode.setTipo((String)jComboBoxTipo.getSelectedItem());
        udao.inserir(umode);
        
        jTextFieldNome.setText("");
        jTextFieldConfSenha.setText("");
        jPasswordFieldSenha.setText("");

        jButtonSalvar.setEnabled(false);
        jButtonCancelar.setEnabled(false);
        jComboBoxTipo.setEnabled(false);
        jTextFieldNome.setEnabled(false);
        jTextFieldConfSenha.setEnabled(false);
        jPasswordFieldSenha.setEnabled(false);
        jButtonPesquisa.setEnabled(true);
        jTextFieldPesquisa.setEnabled(true);
        
         PreencherTabela("Select *from usuario order by Nome");
      
       }
         else{
           umode.setCodUsuario(Integer.parseInt(jTextFieldCod.getText()));
        umode.setNome(jTextFieldNome.getText());
        umode.setSenha(jPasswordFieldSenha.getText());
        umode.setTipo((String)jComboBoxTipo.getSelectedItem());
        udao.update(umode);
        
        jTextFieldNome.setText("");
        jTextFieldConfSenha.setText("");
        jPasswordFieldSenha.setText("");

        jButtonSalvar.setEnabled(false);
        jButtonCancelar.setEnabled(false);
        jComboBoxTipo.setEnabled(false);
        jTextFieldNome.setEnabled(false);
        jTextFieldConfSenha.setEnabled(false);
        jPasswordFieldSenha.setEnabled(false);
        jButtonPesquisa.setEnabled(true);
        jTextFieldPesquisa.setEnabled(true);
        jButtonNovo.setEnabled(true);
        
         PreencherTabela("Select *from usuario order by Nome");
      }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

