    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if(jTextFieldNome.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Preecha o campo nome");
            jTextFieldNome.requestFocus();
        }else  if(jTextFieldEndereco.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null,"Preecha o campo Endereco");
            jTextFieldEndereco.requestFocus();
        }else if(jTextFieldTelefone.getText().isEmpty()){
        JOptionPane.showMessageDialog(null,"Preecha o campo Telefone");
            jTextFieldTelefone.requestFocus();
        }else
      
      if(flag==1){
        fmode.setNome(jTextFieldNome.getText());
        fmode.setEndereco(jTextFieldEndereco.getText());
        fmode.setTelefone(Integer.parseInt(jTextFieldTelefone.getText()));
        fdao.inserir(fmode);
        
        jTextFieldNome.setText("");
        jTextFieldEndereco.setText("");
        jTextFieldTelefone.setText("");

        jButtonSalvar.setEnabled(false);
        jButtonCancelar.setEnabled(false);
        jTextFieldNome.setEnabled(false);
        jTextFieldEndereco.setEnabled(false);
        jTextFieldTelefone.setEnabled(false);
        jButtonPesquisa.setEnabled(true);
        jTextFieldPesquisa.setEnabled(true);
        
         PreencherTabela("Select *from Fornecedor order by Nome");
      
       }else{
        fmode.setCodFornecedor(Integer.parseInt(jTextFieldCod.getText()));
        fmode.setNome(jTextFieldNome.getText());
        fmode.setEndereco(jTextFieldEndereco.getText());
        fmode.setTelefone(Integer.parseInt(jTextFieldTelefone.getText()));

        fdao.update(fmode);
        jTextFieldCod.setText("");
        jTextFieldNome.setText("");
        jTextFieldEndereco.setText("");
        jTextFieldTelefone.setText("");
        
        jButtonSalvar.setEnabled(false);
        jButtonCancelar.setEnabled(false);
        jTextFieldNome.setEnabled(false);
        jTextFieldEndereco.setEnabled(false);
        jTextFieldTelefone.setEnabled(false);
        jButtonNovo.setEnabled(true);
        
         PreencherTabela("Select *from Fornecedor order by Nome");
        
    }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

