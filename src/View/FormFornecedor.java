    private void jTableForncedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableForncedorMouseClicked
       String nome=""+jTableForncedor.getValueAt(jTableForncedor.getSelectedRow(),1);
       con=BDconexao.getconnection();
        con1=BDconexao1.getconnection();
       conex.executaSql("Select *from Fornecedor where Nome='"+nome+"'");
       try {
           conex.rs.first();
           jTextFieldCod.setText(String.valueOf(conex.rs.getInt("CodFornecedor")));
            jTextFieldNome.setText(conex.rs.getString("Nome"));
           jTextFieldEndereco.setText(conex.rs.getString("Endereco"));
           jTextFieldTelefone.setText(String.valueOf(conex.rs.getInt("Telefone")));
         
       } catch (SQLException ex) {
          JOptionPane.showMessageDialog(null,"Erro Ao preecher do MauseClicked");
       }
       BDconexao.fecharConexao(con);
       jButtonEditar.setEnabled(true);
       jButtonExcluir.setEnabled(true);
       
       
    }//GEN-LAST:event_jTableForncedorMouseClicked

